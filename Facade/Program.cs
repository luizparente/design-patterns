namespace Facade {
	class Program {
		static void Main(string[] args) {
			/*
			
			FACADE:
			 
			Facade is a structural design pattern that provides 
			a simplified interface to a library, a framework, 
			or any other complex set of classes. 

			A facade is a class that provides a simple interface 
			to a complex subsystem which contains lots of moving parts. 
			A facade might provide limited functionality in comparison 
			to working with the subsystem directly. However, it includes 
			only those features that clients really care about.

			Having a facade is handy when you need to integrate your 
			app with a sophisticated library that has dozens of features, 
			but you just need a tiny bit of its functionality.

			In this example, let's assume the system needs to intake
			videos uploaded by users, compress them, and store them 
			in a database. There are many moving parts to this process.
			For example, a video encoding service and/or SDK may be needed.
			In addition, a compression service, and a database service are
			required to attain the final goal.

			Instead of requiring users (developers) to interact with all
			of these different services and SDK's, a facade can be created
			to simplify the whole process.

			This pattern is especially useful for SDK developers.
			 
			*/
		}
	}
}
