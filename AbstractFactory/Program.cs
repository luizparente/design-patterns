			ABSTRACT FACTORY:
			 
			Abstract Factory is a creational design pattern 
			that lets you produce families of related objects 
			without specifying their concrete classes. 

			As with all Factory patterns, this one is useful when
			strict control is needed as to how classes are instantiated.

			This pattern works best with matrix product structures.

			In a nutshell, the Abstract Factory looks like a version
			of Generalized Factory, without the generics.

			The Generalized Factory is arguably preferred over 
			Abstract Factory. However, since this is a classic
			pattern, it has been added to this project collection.

			Use the Abstract Factory when your code needs to
			work with various families of related products, but 
			you don’t want it to depend on the concrete classes of 
			those products—they might be unknown beforehand or you 
			simply want to allow for future extensibility.
			 
